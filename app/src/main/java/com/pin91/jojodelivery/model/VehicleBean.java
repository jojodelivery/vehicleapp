package com.pin91.jojodelivery.model;


public class VehicleBean {

	private Integer vehicleId;
	private String vehicleOwnerName;
	private String vehicleOwnerEmail;
	private String vehicleOwnerMobile;
	private String vehicleOwnerContactNumber;
	private String vehicleType;
	private String vehicleNo;

	public Integer getVehicleId() {
		return vehicleId;
	}

	public void setVehicleId(Integer vehicleId) {
		this.vehicleId = vehicleId;
	}

	public String getVehicleOwnerName() {
		return vehicleOwnerName;
	}

	public void setVehicleOwnerName(String vehicleOwnerName) {
		this.vehicleOwnerName = vehicleOwnerName;
	}

	public String getVehicleOwnerEmail() {
		return vehicleOwnerEmail;
	}

	public void setVehicleOwnerEmail(String vehicleOwnerEmail) {
		this.vehicleOwnerEmail = vehicleOwnerEmail;
	}

	public String getVehicleOwnerMobile() {
		return vehicleOwnerMobile;
	}

	public void setVehicleOwnerMobile(String vehicleOwnerMobile) {
		this.vehicleOwnerMobile = vehicleOwnerMobile;
	}

	public String getVehicleOwnerContactNumber() {
		return vehicleOwnerContactNumber;
	}

	public void setVehicleOwnerContactNumber(String vehicleOwnerContactNumber) {
		this.vehicleOwnerContactNumber = vehicleOwnerContactNumber;
	}

	public String getVehicleType() {
		return vehicleType;
	}

	public void setVehicleType(String vehicleType) {
		this.vehicleType = vehicleType;
	}

	public String getVehicleNo() {
		return vehicleNo;
	}

	public void setVehicleNo(String vehicleNo) {
		this.vehicleNo = vehicleNo;
	}

}
